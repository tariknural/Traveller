package com.geziyorum.argumentresolver;

public class TokenPhotoBase64 {
	String token;
	String base64ImageFile;
	
	public TokenPhotoBase64(){
		
	}
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getBase64ImageFile() {
		return base64ImageFile;
	}
	public void setBase64ImageFile(String base64ImageFile) {
		this.base64ImageFile = base64ImageFile;
	}
	
	
}
