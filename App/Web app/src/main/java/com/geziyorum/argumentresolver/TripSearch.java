package com.geziyorum.argumentresolver;

public class TripSearch {

	private Integer personalSharingId;
	private String username;
	private String ppName;
	private String location;
	private String explanation;
	private String tripFolderName;
	
	public TripSearch(){
		
	}

	public Integer getPersonalSharingId() {
		return personalSharingId;
	}

	public void setPersonalSharingId(Integer personalSharingId) {
		this.personalSharingId = personalSharingId;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPpName() {
		return ppName;
	}

	public void setPpName(String ppName) {
		this.ppName = ppName;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getExplanation() {
		return explanation;
	}

	public void setExplanation(String explanation) {
		this.explanation = explanation;
	}

	public String getTripFolderName() {
		return tripFolderName;
	}

	public void setTripFolderName(String tripFolderName) {
		this.tripFolderName = tripFolderName;
	}
		
}
